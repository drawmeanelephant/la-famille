func TestWriteExplorerIsByteDeterministicForSameInputs(t *testing.T) {
	dir1 := t.TempDir()
	cfg1 := writeMinimumSite(t, dir1)
	if _, err := Write(cfg1, 7); err != nil {
		t.Fatal(err)
	}
	b1, err := os.ReadFile(filepath.Join(cfg1.OutputDir, "graph", "index.html"))
	if err != nil {
		t.Fatal(err)
	}
	dir2 := t.TempDir()
	cfg2 := writeMinimumSite(t, dir2)
	if _, err := Write(cfg2, 7); err != nil {
		t.Fatal(err)
	}
	b2, err := os.ReadFile(filepath.Join(cfg2.OutputDir, "graph", "index.html"))
	if err != nil {
		t.Fatal(err)
	}
	if string(b1) != string(b2) {
		t.Errorf("explorer index.html not byte-stable across two writes\n--- a ---\n%s\n--- b ---\n%s", b1, b2)
	}
}

