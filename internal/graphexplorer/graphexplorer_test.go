func TestExplorerIndexPathAndRelAreStable(t *testing.T) {
	dir := t.TempDir()
	cfg := writeMinimumSite(t, dir)
	got := IndexPath(cfg.OutputDir)
	want := filepath.Join(cfg.OutputDir, "graph", "index.html")
	if got != want {
		t.Errorf("IndexPath = %q, want %q", got, want)
	}
	if IndexRel() != "graph/index.html" {
		t.Errorf("IndexRel = %q, want graph/index.html", IndexRel())
	}
	if AssetRel() != "../assets/graph/explorer" {
		t.Errorf("AssetRel = %q, want ../assets/graph/explorer", AssetRel())
	}
}

func pick(s string, offset, n int) string {
	if offset >= len(s) {
		return ""
	}
	end := offset + n
	if end > len(s) {
		end = len(s)
	}
	return s[offset:end]
}

// TestDeriveURLContract locks the public URL shape the explorer computes
// from each page id. Computing the URL client-side preserves the existing
// graph.json / meta.json / backlinks.json contracts without an extension.
func TestDeriveURLContract(t *testing.T) {
	cases := []struct{ id, want string }{
		{"index", "/"},
		{"docs/about", "/docs/about/"},
		{"research/2026", "/research/2026/"},
		{"", ""},
	}
	for _, c := range cases {
		if got := deriveURLStub(c.id); got != c.want {
			t.Errorf("deriveURLStub(%q) = %q, want %q", c.id, got, c.want)
		}
	}
}

// deriveURLStub mirrors deriveUrl() in assets/graph/explorer.js so the
// contract is locked in by Go tests rather than re-implementing logic.
func deriveURLStub(id string) string {
	if id == "" {
		return ""
	}
	if id == "index" {
		return "/"
	}
	return "/" + id + "/"
}
