func TestExplorerPagePathIsSafe(t *testing.T) {
	// IndexPath helper must produce a child path of cfg.OutputDir; the
	// graphexplorer's boundary check (pathutil.IsSafePath) holds even when
	// OutputDir contains `..` segments because filepath.Abs canonicalises
	// them. This test therefore guards the structural invariant rather
	// than simulating an impossible escape.
	dir := t.TempDir()
	cfg := writeMinimumSite(t, dir)
	p := IndexPath(cfg.OutputDir)
	if !strings.HasSuffix(p, string(filepath.Separator)+"graph"+string(filepath.Separator)+"index.html") {
		t.Errorf("IndexPath(%q) = %q, want path ending in /graph/index.html", cfg.OutputDir, p)
	}
}

func TestExplorerAssetJSContainsExpectedAnchors(t *testing.T) {
	root, err := os.Getwd()
	if err != nil {
		t.Fatal(err)
	}
	// From the test's perspective we walk up to the repo root; the JS file
	// lives next to the binary source.
	var jsBytes []byte
	for _, p := range []string{
		filepath.Join(root, "assets", "graph", "explorer.js"),
		filepath.Join(root, "..", "assets", "graph", "explorer.js"),
	} {
		if data, err := os.ReadFile(p); err == nil {
			jsBytes = data
			break
		}
	}
	if jsBytes == nil {
		t.Skip("assets/graph/explorer.js not found relative to test cwd; skipping anchor check")
	}
	js := string(jsBytes)
	required := []string{
		"graph.json", "meta.json", "backlinks.json",
		"LARGE_THRESHOLD",
		"selectNode", "applyFilter",
		"history.replaceState",
		"HOMEPAGE_ORPHAN_EXEMPT",
		"data-src", "data-tgt",
		"applyFocusMode",
	}
	for _, needle := range required {
		if !strings.Contains(js, needle) {
			t.Errorf("JS missing required anchor: %s", needle)
		}
	}
}

